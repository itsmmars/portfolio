import { 
  Container, 
  Flex,  
  Stack, 
  VStack,  
  Text,
  Heading 
} from '@chakra-ui/react'
import Head from 'next/head'
import { useEffect, useState } from 'react';
import { Layout } from '../components/base/Layout'


export default function Home() {
  const [fontIndex, setFontIndex] = useState(0);
  // eslint-disable-next-line react-hooks/exhaustive-deps
  const fonts = ["Montserrat", "Helvetica", "Babylonica", "Times New Roman", "Courier New", "Poppins"]

  useEffect(() => {
    const intervalId = setInterval(() => {
      setFontIndex((fontIndex + 1) % fonts.length)
    }, 1000)
    return () => {
      clearInterval(intervalId)
    }
  }, [fontIndex, fonts])

  return (
    <>
      <Head>
        <title>Mike Filicetti: Portfolio</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <Layout hideNavBar={false}>
        <Container maxW="container.xl" centerContent px="0" h="calc(100% - 60px)">
          <Flex 
            direction="row" 
            justify="center"
            align="center"
            w="100%"
            textAlign="center"
            m="auto"
          >
            <VStack justifyContent={`center`} spacing="20px">
              <Heading 
                variant={`small-caps`} 
                size={fonts[fontIndex] === `Courier New` ? `2xl` : `4xl`}
                lang={``}
                style={{ fontFamily: fonts[fontIndex] }}>
                  Mike Filicetti
              </Heading>
              <Stack direction={['column', 'row']} spacing={[`1rem`, `3rem`]} fontSize={[`l`, `xl`]} fontWeight={700}>
                <Text flexWrap={`nowrap`}>
                  Frontend Software Engineer
                </Text>
                <Text flexWrap={`nowrap`}>
                  Product Manager
                </Text>
                <Text flexWrap={`nowrap`}>
                  UI/UX Wizard
                </Text>
              </Stack>
              {/* <Text fontSize={[`l`, `xl`]} fontWeight={700}>
                Frontend Software Engineer | Product Manager | UI/UX Wizard
              </Text> */}
              <Text fontSize={[`sm`, `md`]} py={[`3rem`, `1rem`]}>
                Crafting seamless UI/UX solutions to drive your business forward.
              </Text>
            </VStack>
          </Flex>
        </Container>
      </Layout>
    </>
  )
}
